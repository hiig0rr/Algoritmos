#include <stdio.h>

int main() {
    int numeros[6];

    printf("Digite 6 números possíveis para o sorteio:\n");
    for (int i = 0; i < 6; i++) {
        printf("Digite o %dº número: ", i + 1);
        scanf("%d", &numeros[i]);
    }

    printf("Os números digitados foram:\n");
    for (int i = 0; i < 6; i++) {
        printf("%d ", numeros[i]);
    }
    printf("\n");

    return 0;
}